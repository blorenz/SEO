<p class=MsoNormal style='mso-margin-top-alt:auto; mso-margin-bottom-alt:auto; font-weight: bold;'>10. <u>Mediation and Arbitration</u> </p>
<p align="left"><strong>10.1 </strong><u><strong>Filing a Claim </strong></u><br>
  If a dispute arises under this Agreement, the parties agree to first try to resolve the dispute amongst each other. The claimant (buyer) will notify the respondent (seller) of the dispute and allow the respondent to either agree to refund the item with all fees and documented transportation costs, or reach a mutually acceptable resolution with the claimant or resolve the claim within five business days in accordance with the Arbitration herein. </p>
<p>If it proves impossible to arrive at a mutually satisfactory solution through mediation, the claimant party will submit and online arbitration form which will be assigned to a separate, binding arbitration company. </p>
<p>Arbitration shall not be available for claims involving the following: </p>
<p>a. Any dispute amount $400.00 ( US ) or less (before taxes and excluding any inspection, transportation, and/or GDTD fees)<br>
  b. If the item has been in the possession of the claimant for more than 72 hours<br>
  c. If the auction has been closed for more that 30 (thirty) days </p>
<p>The claimant must file a claim online stating that the respondent misrepresented the condition or description of an item, or failed to make proper disclosure within 72 hours of the item being in the claimant's possession. The online form will be sent to the claimant, the respondent, and the arbitration company which will start the arbitration process. </p>
<p align="left"><strong>10.2 <u>Arbitration Fees</u></strong> <br>
  An initial filing fee of one hundred dollars ($100 US) must be paid by the claimant within 72 hours of the filing of the claim by a method stated when filing the claim. A claim will be considered filed when the initial online arbitration form is submitted, but will not become "active" until confirmation is received that the filing fee has been tendered. </p>
<p>The Arbitrator, or expert appointed by the Arbitrator, will review the auction
  information, and may inspect the item. The Arbitrator may then issue a decision
  or require the claimant and respondent to provide written evidence and written
  submissions along with photos following which the Arbitrator shall make a final
  decision. The Arbitrator may also permit oral submissions by way of teleconference.
  If the Arbitrator finds that the respondent misrepresented the condition of
  the item, or failed to disclose information as required by this agreement with
  respect to a condition or description, where the costs of correcting or repairing
  such condition, or the difference in value of the item as a result of the condition
  is deemed by the Arbitrator exceeds $400.00 before tax, inspection, transportation,
  and/or GDTD fees (the &quot;Deemed Amount&quot;), then the Arbitrator shall
  order that the respondent refund the auction close amount, all documented transportation,
  inspection, and GDTD fees associated with the transaction, or adjust the close
  auction price to a mutually agreed upon price. The respondent will also be assigned
  the responsibility to pay any and all Arbitration costs, including the initial
  $100 filing fee. If the Deemed Amount is less than $400.00, or the Arbitrator
  finds no action against the respondent, the Arbitrator shall dismiss the claim
  and direct that the claimant pay all costs of the Arbitration. </p>
<p>If an arbitration hearing is conducted pursuant to the above mediation process, and the claimed amount is less than three thousand dollars ($3,000 US), an additional fee of one hundred dollars ($100 US) shall be paid by both the claimant and the respondent. If the claimed amount is three thousand dollars ($3,000 US) or more, then the additional fee to both the claimant and the respondent will be five hundred dollars ($500 US). </p>
<p align="left"><strong>10.3 <u>Arbitration Process</u></strong> <br>
  The claimant and respondent shall be notified of the date, time and method of hearing by fax or email. The hearing shall be conducted by telephone with all parties participating and having an ability to hear the testimony of the other party and to hear the assigned arbitrator. At this hearing, the claimant shall proceed first and shall explain the claim and give justification or reasons to support it. The claimant may have other persons with knowledge of the claim offer their verbal statement. The respondent shall proceed after the conclusion of claimant's presentation and shall offer any justification or reasons that the claim is not accurate or that an award is not appropriate. The respondent may have other persons with knowledge of the claim offer their verbal statement as well. An electronic record shall be made of all oral statements of the parties and other persons. </p>
<p>At the conclusion of the presentations of the parties, the arbitrator shall consider all the material presented by the parties, their statements, and the statements of other persons made during the hearing and shall issue a final and binding decision in writing that shall decide the issues presented based solely upon the information presented by the parties and the law of the jurisdiction governing the contract or dispute. The arbitration decision shall briefly explain the reason for the award. The decision shall be issued and faxed or emailed to the parties within 24 hours of the arbitration hearing. </p>
<strong>10.4 <u>Appointment of Arbitrator </u></strong><br>
<p>The Arbitrator shall, through the program administrator, appoint one of the jurists serving on the panel as the sole arbitrator for each case. A jurist is a properly licensed attorney in any state who has previous experience as a judge on a state or federal court which had subject matter jurisdiction of the same types of disputes submitted under this program. The jurist appointed shall certify to the parties that the jurist has no conflict of interest, bias, prejudice or interest in any claim referred to them. The jurist selected shall be paid by the arbitration company a fee reflective of the time spent resolving the dispute submitted regardless of its outcome. </p>
<p>If a party objects to the appointment of a jurist, that objection shall be filed within 24 hours of their appointment and shall be resolved by the program administrator immediately. The decision of the program administrator is final and binding upon the parties in all 50 states in the United States . </p>
<p align="left"><strong>10.5 <u>Appointment of Costs and Fees </u></strong><br>
  The arbitrator may in their discretion award filing fees and all costs associated to the case as cost to a prevailing party in the arbitration award. Such decisions are final and binding and become part of the award. </p>
<p align="left"><strong>10.6 <u>Arbitration Judgment</u></strong> <br>
