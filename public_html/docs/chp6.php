<p align="center" class="style1"><strong>Add an Item</strong></p> 

<p class="MsoNormal">Adding items to your online inventory has been simplified
  and streamlined as much as possible with the ability to gather as much information
  for searching.&nbsp; This is also the first part of the selling process.&nbsp; You
  will need the item loaded in your online inventory before an auction can be
  created.&nbsp; You can have an automated feed setup with a 3rd party company
  to help streamline this process even further. Most data about the item and
  pictures can be automatically fed into your inventory.</p>
<p class="style2"><a name="additemsman">Add Items Manually </a></p>
<p>From the Control Panel, click on “<b>Add a Wholesale Item </b>.”</p>
<p class="MsoNormal">The <strong>Add a Wholesale Item</strong> screen now displays
  as shown in fig 6.1.</p>
<p class="MsoNormal"><img src="images/AddItem.gif" width="522" height="430"><br>
    <i>fig 6.1</i></p>
<p class="MsoNormal">Depending on the category you choose in the first box, the
  following box will appear with information relevant to that category.&nbsp; Continue
  through the boxes choosing the appropriate selections relating to your item
  until the remaining box(es) display "**Finished**" and there is a Continue
  button. Click the Continue button at the bottom of the screen.&nbsp; </p>
<p class="style2"><a name="description">Description Form</a> </p>
<p class="MsoNormal">The next
    step is filling out the Item Description Form as shown in figure 6.2.&nbsp; This
    form asks questions about the type of item you selected in the category list.&nbsp; Depending
    on what you chose, the form will adjust relating
    to that industry and item type.</p>
<p class="MsoNormal"><img src="images/edititem.gif" width="594" height="754"></p>
<p class="MsoNormal"><img src="images/edititem1.gif" width="640" height="592"></p>
<p class="MsoNormal"><i>fig 6.2</i></p>
<div align="center"></div>
<p class="MsoNormal">The first question is the Auction Title. This is the title
  of your auction and will display in the list when searching. This is the first
  look at your auction and bidders must click on this entry to view and bid on
  your auction. </p>
<p class="MsoNormal">Continue filling out each field that correctly relates to
  the item you are listing. Any fields with a red star before MUST be filled
  out. This is necessary for listing and searching purposes.</p>
<p class="MsoNormal">The description field is available to describe your item
  in more detail. This is where you will enter any other information about the
  item not covered by the other fields. </p>
<p class="MsoNormal">**REMEMBER**&nbsp; The more information you enter, the better
  your auction will sell. A good rule of thumb is entering what you would like
  to see and read in an auction that you would bid and purchase on. Do not enter
  condition information in the Description field. The condition section is next. </p>
<p class="MsoNormal">The Item Location section shows your address location by
  default. If the item is not physically located at that address, you can to
  enter the actual city, state, and zip of the item.</p>
<p class="MsoNormal">The comments section is not necessary to enter information
  and will not show up in the auction. This section is for your records only.</p>
<p class="MsoNormal">Once completed, click the continue button to proceed to
  the Condition Report. </p>
<p class="MsoNormal">**NOTE**&nbsp; If there are any errors on the form, a red
  message will display at the top of the form explaining the error(s). Correct
  the error(s) and click Continue to proceed to the Condition Report.</p>
<p class="MsoNormal">**NOTE**&nbsp; You do not have to continue with this item.
  It has been entered and stored in your Active Items. This is useful if you
  need to enter many items and then later want to conduct the Condition Report. </p>
<p class="MsoNormal">&nbsp;</p>
<p class="style2"><a name="condition">Condition Report</a></p>
<p class="MsoNormal">The next page that comes up gives you the opportunity to
  enter a detailed Condition Report. You can mark each part of the item as either
  excellent, good, average, poor or not applicable as you see fit. It is divided
  into 3 categories, which are the mechanics, the interior, and the
  exterior condition. Depending on which category was chosen, you will have different
    options available on the condition form but they all have a similar format.
    You may also enter comments about why you chose the particular ranking. At
    the end, there is also a required field where you can enter in general
  information about the condition of the item. See figure
  6.3 below to see this page. </p>
<p class="MsoNormal"><img src="images/condition.gif" width="481" height="609"></p>
<p class="MsoNormal"><img src="images/condition1.gif" width="515" height="759"></p>
<p class="MsoNormal"><em>fig 6.3 </em></p>
<p class="MsoNormal">At the top of the page is the link &quot;Printable Condition
    Report&quot; which
    you can use to print out a copy of your condition report. </p>
<p class="MsoNormal">If you have photos to upload,  click on the yes option,
  and click submit. If you do not have photos, or the photos are already uploaded
  for this item, click on the nooption, and click submit. You can
  always add and delete photos later (deleting photos can only be done BEFORE
  the item goes into auction). </p>
<p align="center" class="huge">&nbsp;</p>
<p class="style2"><a name="pictures" id="pictures"><span style="font-size: 16pt;">Uploading
        Photos</span></a> </p>
	<p class="MsoNormal">After you create an item, enter the description and condition
	  report in the database, you can upload pictures by the <b>Manage Photos</b> page
	  shown in fig 6.4 below. </p>
	<p class="MsoNormal"><img src="images/ManagePhotos.gif" width="515" height="369"><br>
	  <em>fig 6.4
    </em>	</p>
	<p class="MsoNormal">Here, you will need to know the location of the graphic
      files of the item you want to enter. Click on the Browse button and select
      the file. You will then see the location displayed as in fig 6.4 above.
      You can upload up to five (5) pictures of the item at a time, but you have
      the availability to upload as many pictures as you like at no additional
      costs or fees.</p>
    <p class="MsoNormal">Click the <b>Upload</b> button and the picture(s) will
      be uploaded to the server. You can immediately see the image(s) once they
      have been uploaded so you can verify you have uploaded the proper picture. </p>
    <p class="MsoNormal">Here you can manage captions of your pictures, delete
      pictures, and choose which photo will be the Main Photo. The Main Photo
      will be displayed first in the auction and as a thumbnail on the list of
      auctions. To edit the captions for the pictures, simply click on the link
      under the picture you want to edit that says, “<b>edit
      caption</b>”. Reference fig 6.5 below as an example.</p>
  <p class="MsoNormal"><img src="images/EditPhotos.gif" width="519" height="854"> </p>
    <p class="MsoNormal"><i>fig 6.5</i></p>
    <p class="MsoNormal">When you have completed the captions and selections,
      simply click the <b>Update Photos</b> button at the bottom of the page.
      All changes that you have made will be entered into the database. You can
      click <b>Edit
      This Item</b> to
      go back to the description page, <strong>Add More Photos</strong> to add
      more photos of your item, or <strong>Create This Auction </strong>to start
      the auction. To view your entry, click on <b>My Items </b> on the control
      panel to see your new listed entry. Refer to fig 6.6 for our example of
      this entry.</p>
    <p class="MsoNormal"><img src="images/auctionitem.gif" width="962" height="100"><i>    fig 6.6</i></p>
    <p class="MsoNormal">From here, you can verify that your item has been successfully
      entered into the database. You can click on the image to add or edit the
      pictures for your item, the edit link to edit the item description and
      condition report, the remove link to remove the item from your list of
      items, or the create auction link to create an auction for the item. You
      can also click the preview item link to see what the auction will look
      like. For the items already in auctions, we cannot see the create auction
      link or the remove link. If there is no edit link, then that means that
      your reserve has been met. Once the reserve is met, some items cannot be
      changed. You will always have the ability to add photos to your item regardless
      of the status of the auction.</p>
    <p class="style2"><a name="autofeeds">Automatic Feeds</a></p>
    <p class="MsoNormal">3rd party data and photos are excepted in the system
      to be automatically fed every night. Since most of the data will be entered
      for you, there are a few items that will still need to be selected manually
      before your item is ready for auction. </p>
    <p class="MsoNormal">Most feeds do not send condition reports and there might
      be some description information that will need to be added. Depending on
      what the 3rd party company gathers will determine what will need to be
      manually edited. See figure 6.7 below for an example of the Items screen
      after an automatic feed. </p>
    <p align="left" class="MsoNormal"><img src="images/autofeed.gif" width="961" height="316"><em>fig
      6.7 </em></p>
    <p align="left" class="MsoNormal">All the items have pictures and many of
      the description items have been entered including the Auction Title. Notice
      the Create Auction link says finish adding in red. The system understands
      that the vehicle does not have all the information necessary to create
      an auction. You must click on the finish adding link and complete what
      is missing for the item(s) you are wanting to put into auction. Continue
      finishing by editing the condition report.</p>
    <p align="left" class="MsoNormal">To setup your dealership with automatic
      feeds, your feed provider will need to send Go DEALER to DEALER your daily
      feed. Most 3rd party companies will need your authorization to make this
      complete. Our engineers will ensure your inventory will be available in
      your My Items section.</p>
