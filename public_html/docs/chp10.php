  <p align="center" class="style1 style2"><b><span style="; font-weight: bold;">Arbitration</span></b></p> 
  <p class="MsoNormal">Our easy and fast Arbitration process is conducted online by clicking on the Arbitration link in the Control Panel. This process is to be used only if there is a 


 legitimate

 issue regarding an auction that has closed with a winning bid or an auction has ended due to a Make Offer has been accepted.</p>
  <p class="MsoNormal">Before the Arbitration process may continue, the other party MUST have the opportunity to correct the issue first. If no mutual corrections can be made, and the damage in question is more than $400.00 (U.S.) before taxes, then the auction may be Arbitrated using this online process.</p>
  <p class="MsoNormal">Once the Arbitration link is selected, a disclaimer page is shown. Click the button below that you have read and understand the information and the Arbitration Policy.</p>
  <p class="MsoNormal"><a name="Chp10_step1" id="Chp10_step1"></a><span class="style3">Step 1: Select Auction </span></p>
  <p class="MsoNormal">A list of auction will display showing you all the auction which have closed within the past 30 days. (NOTE - any auctions which have closed more than 30 days ago are no longer available for Arbitration). See fig 10.1 below for an example of Step 1. </p>
  <p class="MsoNormal"><em><img src="images/step1.gif" width="992" height="606"><br>
  fig 10.1</em></p>
  <p class="MsoNormal">In this example, we have selected the last item in the list, the 2005 Chevrolet K1500. You can see who the seller's dealership is and how much was paid. The auction number is also visible with the start and end date of the auction.</p>
  <p class="MsoNormal"><a name="Chp10_step2" id="Chp10_step2"></a><span class="style3">Step 2: Date of Possession </span> </p>
  <p class="MsoNormal">Once an auction is selected, Step 2 asks you the date you took possession of the item. It would be in your best interest if you could provide some documentation to support this when the Arbitration company investigates. See fig 10.2 below to see Step 2.</p>
  <p class="MsoNormal"><img src="images/step2.gif" width="993" height="315"><br>
  fig 10.2</p>
  <p class="MsoNormal">Click the calendar icon to the right and select the exact day your dealership took possession of the item and click the Continue button to proceed to Step 3. </p>
  <p class="MsoNormal"><a name="Chp10_step3" id="Chp10_step3"></a><span class="style3">Step 3: State the Problem </span></p>
  <p class="MsoNormal">This step is where you will describe the issue and enter any extra fees that were associated with this item. All fees MUST have documentation to support the claim. Also, enter the dates when you contacted the seller to negotiate a mutually agreed settlement and their response. See fig 10.3 below for an example of Step 3.</p>
  <p class="MsoNormal"><img src="images/step3.gif" width="1017" height="331"><br>
    <em>fig 10.3</em></p>
  <p class="MsoNormal">Once the information has been entered. Select the Continue button to proceed to Step 4.</p>
  <p class="MsoNormal"><a name="Chp10_step4" id="Chp10_step4"></a><span class="style3">Step 4: Appropriate Resolution </span> </p>
  <p class="MsoNormal">This step is where you will list what you would like to see as an appropriate resolution to the issue. It is a good idea to give a few options that you will accept as solutions. See fig 10.4 below for an example of Step 4.</p>
  <p class="MsoNormal"><img src="images/step4.gif" width="439" height="279"><br>
    <em>fig 10.4</em></p>
  <p class="MsoNormal">After you complete your text, click the Continue button to proceed to Step 5.</p>
  <p class="MsoNormal"><a name="Chp10_step5" id="Chp10_step5"></a><span class="style3">Step 5: Review Information </span> </p>
  <p class="MsoNormal">This step is the final step in the Arbitration 


 procedure. This screen will show you the exact claim form that will be submitted including all the Auction information, description, condition report (not shown in example below), the Claimant and 


 Respondent's

 information, and your Complain and Proposed Resolution. See fig 10.5 below for an example.</p>
  <p class="MsoNormal"><img src="images/step5.gif" width="1008" height="650"><br>
    <em>fig 10.5</em></p>
  <p class="MsoNormal">At the end of this form is information explaining any fees that will be associated with this claim and a notice that the claimed damages MUST be more than $400.00 to be 


 eligible

 for arbitration. </p>
  <p class="MsoNormal">This information will be sent to you, the 


 respondent

 and the Arbitration company who will facilitate the rest of the Arbitration process. Click the Submit Arbitration Claim button to finalize the application.</p>
  <p class="MsoNormal">The Arbitration company will be in contact with you within 2 business days to proceed further.</p>
