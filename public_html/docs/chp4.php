  <p align="center" class="style1"><span style="">Searching</span></p> 
  <p class="MsoNormal">Searching the site for a desired  item can be accomplished
  two ways. One way to search is to use the Standard Search. The other way is
    to use Advanced search. See the
    Search Bar in figure 4.1 below.</p>
  <i><img src="images/searchbar.gif" width="599" height="25">fig 4.1</i>
<p class="style2"><a name="Standardsearch" id="Standardsearch"><span style="font-size: 16pt;">Standard
        Search</span></a></p> 
  <p class="MsoNormal">The standard search uses the textbox and the regular Search
    button as well as the Auction ID and Vehicle ID buttons. You can type in
    one or more keywords to search by. Figure 4.2 below shows the standard search
    part of the search bar. Searching can be done directly to the Auction ID.
    If you saw an auction that you like, you can either click on &quot;Add to
    my watch list,&quot; which
    is shown in <a href="chp5_place.php"><em><strong>Placing
    Bids</strong></em></a>, or you can remember the Auction ID number (Shown
    in the Auction title area). You can type the Auction ID into the textbox
    and click Search Auction ID to find the auction again. If the item is NOT
    in an active action and is a Request Aution, you must enter the number and
    click the Vehicle ID button to find that item again. </p> 
  <p class="MsoNormal">  &nbsp;<img src="images/standardsearch.gif" width="445" height="25">&nbsp;<i>fig 4.2</i></p>
  <p class="style2"><a name="Advancedsearch" id="Advancedsearch"><span style="font-size: 16pt;">Advanced Search</span></a></p> 
  <p class="MsoNormal">The Advanced Search allows you to search one
    or multiple items in a category at the same time, plus you can narrow your
    search down to the exact items. To conduct an Advanced Search, click
    the Advanced Search button in the upper left part of the screen. You will
    see the list of categories (industries) shown in figure 4.3 below. You
must    choose a specific category to continue. We will use Passenger Vehicle
    for this document. See fig 4.4 below for an example of the advanced search
    page.</p>
  <p class="MsoNormal"><i><img src="images/category.gif" width="204" height="222"><br>
    fig
      4.3</i></p>
  <p class="MsoNormal style4"><span class="MsoNormal">Once you select a category
      you would like to search, you may choose one or multiple items from the
      subcategories below it. Only items with listed inventory will be available
      with th number of items listed in that section. We used on Passenger
      Vehicles, so the checkboxes shown will be manufactures of the passenger
      vehicles industry. Below in fig 4.4 is an example of what you will see.
      We will use Chevrolet and Ford to search for both manufactures at the same
  time. </span></p>
  <p class="MsoNormal style4"><img src="images/advsearch1.gif" width="544" height="433"><br>
    <em>fig
      4.4</em> <br>
      <br>
  </p>
  <p class="MsoNormal">When you press continue, you will see the subcategories,
    if any, for the categories you chose using the checkboxes above. In this
    case, you will see the Chevrolet and Ford models. As shown below in fig 4.5,
    the models of the items are shown at the top of the page. Below that you
    can chose other options to search by. All is selected by default. If you
    do not change this, then all the  items will searched.
    Below that are more specifications to search by. This list is customized
    to the category that you pick. However, the U.S. map at the bottom is present
  in all searches.</p>
