  <p class="style2"><a name="Chp7_Arbitration" id="Chp7_Arbitration"><span style="">Arbitration</span></a></p>
  <p class="MsoNormal">The Arbitration link is used if there is an auction where the Arbitration process is necessary. The first screen will display a disclaimer informing you how the arbitration process will work. This is a 5 step process that will document the auction, the date you received the item in your 


 possession, the issue in question, an appropriate resolution to the issue, and the final submittal.</p>
  <p class="MsoNormal">Before the Arbitration process can be started, the other party MUST have the opportunity to resolve the issue. The price of the damage must be more than $400.00 (U.S.) and was NOT disclosed in the auction details and condition sections. </p>
  <p class="MsoNormal">See the <a href="chp10.php">Arbitration</a> section for a complete step-by-step walk through of the Arbitration Process. </p>
