  <p class="style2"><a name="Chp7_Unsettledfees"><span style="">My
        Invoice History </span></a></p>
  <p class="MsoNormal">This screen displays all of your dealership’s current
    Go DEALER To DEALER service fees that are due for payment. Payments are made
    


 electronically

 once a month. When your payment is processed, the transactions
    lines will be updated from UNPAID to PAID.&nbsp; Please
    reference fig 7.6 to see the example of the Online Invoice. You can
    view previous months invoice to see the fees for that month. Invoices are
    kept online for your review for up to one year. As soon as a transaction
    is made, the entry will be 


 immediately

 displayed on the invoice. The running
    total is shown at the lower right corner of the report. You can click on <strong>Unsettled
    Fees</strong> to
    see fees that are pending approval. </p>
  <p class="MsoNormal"><img src="images/Invoice.gif" width="1013" height="222">  <i>fig 7.6</i></p>
  <p class="MsoNormal">Under the column for Type, you can see what kind of
    fee the transaction generated. The report will also display the username
    who generated the fee and at what date, along with the actual auction number
    and the title. The status column will show if the fee has been paid or unpaid. </p>
