<p align="center" class="style1"><b><span style=""><span style=";">Auctions</span></span></b></p>
<p class="style2"><a name="relist" id="relist">Relist</a></p>
<p class="MsoNormal">To &quot;relist&quot; an auction, navigate to the My Wholesale Auctions
  page in the Control Panel. Then select closed auctions to relist an auction
  that has been through an auction before. Once the auction ends, it will be
  stored in the closed auctions area. See the closed auctions screen below in
  fig 6.15.</p>
<p class="MsoNormal"><img src="images/ClosedAuctions.gif" width="961" height="599"><em>fig
  6.15</em></p>
<p class="MsoNormal">View the auction that you would like to relist in another
  auction. If any of the closed auctions have already been re-listed, notice
  the option under the thumbnail that displays &quot;currently re-listed&quot;. All auctions
  that can be re-listed are shown with a red link under the thumbnail that displays
  &quot;re-list auction&quot;.</p>
<p class="MsoNormal">Click the re-list auction link to open the Create a Wholesale
  Auction screen for that item as shown below in fig 6.16.</p>
<p class="MsoNormal"><img src="images/relist.gif" width="633" height="825"><br>
  <em>fig 6.16</em></p>
<p class="MsoNormal">Notice that all the values from the previous auction are
  kept. This makes re-listing the auction quick and easy. The start and end times
  will start at the very next available time and end at the default 7 days. You
  can still adjust all the options for the auction if needed. </p>
<p class="MsoNormal">Once your settings are set for the auction, simply click
  the Please Create My Auction button at the bottom of the page. </p>
<p class="MsoNormal">&nbsp;</p>
<p class="style2">Editing a Live Auction<a name="editauction"></a></p>
<p class="MsoNormal">When editing a &quot;LIVE&quot; auction (an auction that is currently
  running in Open Auction status), some options can be changed and others cannot.
  This also depends on if there are bids made and if the reserve has been met.</p>
<p class="MsoNormal">Regardless if there are bids or not, pictures can ALWAYS
  be added but not deleted and the auction start and end times cannot be altered. </p>
<p class="MsoNormal">If there are NO bids on the item, then the reserve has not
  been met either. Everything about the item's description and condition can
  be updated in the My Items section. The auction prices can only be LOWERED.</p>
<p class="MsoNormal">If there has been a bid on the auction then all description
  and condition settings are locked in. You can add to the description and condition
  by clicking the Edit link in the My Wholesale Auctions section. See fig 6.17
  below for an example. As well as lower your reserve and buy now prices. </p>
<p class="MsoNormal"><img src="images/editauction.gif" width="990" height="623"><br>
  <em>fig 6.17</em></p>
<p class="MsoNormal">There will be a date and time stamp on the auction next
  to the entries you have added as shown in fig 6.18 below. </p>
<p class="MsoNormal"><img src="images/editauction1.gif" width="746" height="310"><em>fig
  6.18</em></p>
<p class="MsoNormal"> If the auctions reserve HAS been met or exceeded, you can
  still add to the description and condition report as shown above and also have
  the ability to lower your Buy Now value. You will not have the ability to PULL
  the auction since an auction with a reserve which has been met is considered
  a sold item. You will also not have the link for Next Bid Wins since the reserve
  has already been met or exceeded. <br>
</p>
