    with. There are a few links that you can select..&nbsp; These will be explained
    in more detail in other chapters. Refer to fig 3.2 for the example of the
    Main Menu Bar.</p>
  <p class="MsoNormal"><img src="images/menubar.gif" width="388" height="19">&nbsp;&nbsp;<i>fig
    3.2</i></p>
  <p class="MsoNormal">The first link is <b>HOME</b>. Wherever you are in the
    site, you can click this link to return to the User’s Home Page as described
    in the <b><i><a href="chp2.php">User’s Home Page</a></i></b> section. </p>
  <p class="MsoNormal">Next is the <b>CREATE AN AUCTION</b>. This section will
