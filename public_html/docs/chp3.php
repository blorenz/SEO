  <p class="MsoNormal">Next is the <b>HELP</b> link. This link is where you will
    find this Help document online.&nbsp; This information is designed to help
    guide you in the use of the site. Other informational items are found here
  including updated information, user agreements, and policies.</p>
  <p class="MsoNormal">Next is the <b>CONTACT US</b> link. This area is used
    for your feedback and suggestions. A form will be displayed for you to fill
    out. If you have questions or issues, please use this form to let us
    know. More detail is given about this area in the <b><i><a href="chp9.php">Contacting
    Us</a></i></b> section. </p>
  <p class="MsoNormal">Last is the <b>LOGOUT</b> link. By clicking this link, you will be securely logged out of the site. It is good practice to logout when you are away from your computer (especially if your computer does not have a lock feature). Staying logged in does not affect the auctions but if you leave your machine, you may leave your information open and unsecured. The best practice is to logout when you are finished or you are going to leave the computer for any length of time. After clicking this link, you will be placed back to the logon screen.</p>
