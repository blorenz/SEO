bidding.</p> 
  <p class="MsoNormal">This screen lets you see all of your bids for any current

auctions. You can also see if you are winning, losing or if your bid did not
    meet the reserve of the auction. An example is shown in Fig

5.4 below.</p>
  <p class="MsoNormal"><img src="images/BidsOpenAuctions.gif" width="700" height="373"><br>
  &nbsp;<i>fig 5.4</i></p>
  <p class="MsoNormal">This example shows 2 bids are winning, 1 is losing and
    1 did not meet the reserve. This means that another user 

has  set a maximum bid which is above our bid.&nbsp; We can see  all the users’ bids
in the 

dealership here by viewing who made the bid in the Bidder column.. Note: Once
an auction closes this information will no longer appear in this screen.</p>
  <p class="style2"><a name="makeoffertoseller" id="makeoffertoseller"><span style="">Make
  Offer to Seller </span></a></p>
  <p class="MsoNormal">When the auctions from the previous (Bids for Open Auctions)
    section end, they 

appear here as Closed Auctions. This section allows you to see a history of what
    you or anyone else in your 

dealership has bided on and what the ending status was. This section
    is only for the auctions that have<span 

style="font-size: 18pt;"> </span>closed.<span style="font-size: 18pt;">&nbsp; </span>If
    you have 

won an auction, please refer to <b><i><a 

href="chp8.php">Winning the Auction</a></i></b> for more 

information on how to finalize the transaction.  </p>
  <p class="MsoNormal">If you were the highest bidder of an auction but did not
    meet the reserve, you will have the ability to Make an Offer to the Seller.
    By going to the Bids for Closed Auctions, as shown in fig 5.5 below, you
    will see a Make Offer link to the left of an auction that you did not make
    the reserve but were the highest bidder when the auction closed. </p>
  <p class="MsoNormal"><img src="images/makeoffertoseller.gif" width="962" height="363"><br>
  <i>fig 5.5</i></p>
  <p class="MsoNormal">To make an offer to the seller, simply click the Make
  Offer link. From here, you will see the final bid, the seller's reserve and
    an area to type in an offer to be sent to the seller. If your offer is accepted
    by the seller, the item will be considered sold. If a decline is selected
    by the seller, you will receive an email stating such. See fig 5.6 below
    to view an example for the Make Offer to the Seller.</p>
  <p class="MsoNormal"><img src="images/makeoffertoseller1.gif" width="543" height="637"><br>
    <em>fig 5.6</em></p>
  <p class="MsoNormal">After you make this offer, you will have an alert in the
    Control Panel that states &quot;Make Offer Pending&quot; in red as shown below in fig
    5.7.</p>
  <p class="MsoNormal"><img src="images/makeofferpending.gif" width="252" height="161"></p>
  <p class="MsoNormal">    <em>fig 5.7</em></p>
  <p class="MsoNormal">This is here if you wish to &quot;retract&quot; your offer
    to the seller. To


 proceed

 to retract the offer, simply click the Make Offer Pending
    link in the Control Panel and click the button at the bottom &quot;Retract your
    Current Offer&quot;. This option will end the offer and will NOT allow another
    offer to be created for this auction. The offer option is over for this auction.</p>
  <p class="MsoNormal">If the seller &quot;counters&quot; your offer, you will receive
    an email and an alert in your Control Panel as shown below in fig 5.8.</p>
  <p class="MsoNormal"><img src="images/alert.gif" width="252" height="175"><br>
    <em>fig 5.8</em></p>
  <p class="MsoNormal">By selecting the Alert link in the Control Panel, you
    will see which items are Counter Offers. Click on the Counter Offer in question.
    From here you will see some details about the auction, the final bid that
    was made, the reserve, and the amount offered from the seller. See fig 5.9
    below for an example of this page.</p>
  <p class="MsoNormal"><img src="images/makeoffertoseller2.gif" width="545" height="600"><br>
    <em>fig 5.9</em></p>
  <p class="MsoNormal">  If you agree with this amount, simply click
      the Accept (BUY) button and the item will be considered sold. You may also
    Counter the seller's offer and the process will continue back and forth until
    either the Accept or Decline is chosen. If you choose to Decline, the process
    is ended and all make offer options for this auction are over. </p>
